//  O que é Interface?

//  A Interface se assemelha, até certo nível, com uma classe. Ela funciona como uma espécie de "contrato" que uma classe assume. 
//  Ao informar que a classe irá implementar a Interface, diz-se que a classe irá receber e implementar os métodos contidos na Interface. 
//  Ao definirmos que uma classe irá implementar uma Interface, deveremos implementar na classe,
//  todos os métodos contidos na Interface. Caso contrário, o funcionamento não irá ocorrer corretamente.
