
public class Literais {
	public static void main(String[] args) {
		// Nesta seção de estudos iremos conhecer os conceitos acerca de literais.
		
		/** 
		 * O que são literais?
		 * 
		 * Literais no Java, são a representação direta de valores fixos no código-fonte. Podemos utilizá-los para inicializar, por exemplo, uma variável.
		 * 
		 * Como exemplo, podemos declarar uma variável que irá receber a idade de um usuário. E na declaração desta variável, 
		 * podemos definir que ela irá ser inicializada com o valor zero. E após o usuário informar sua idade, o valor da variável será alterado.
		 * 
		 * Vejamos abaixo este exemplo.
		 * 
		 * */
		
		int idadeUsuario = 0;
		
		// Após o usuário realizar seu registro no sistema, ele irá informar sua idade e o valor da variável irá ser alterado.
		
		idadeUsuario = 20;
	}
}
